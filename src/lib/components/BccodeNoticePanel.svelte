    <h3>BBCode Formatting Guide</h3>
    <p>You can use the following BBCode tags to format your recipe descriptions:</p>
    <ul>
        <li><code>[b]Bold text[/b]</code> - Makes the text bold.</li>
        <li><code>[i]Italic text[/i]</code> - Makes the text italic.</li>
        <li><code>[u]Underlined text[/u]</code> - Underlines the text.</li>
        <li><code>[s]Strikethrough text[/s]</code> - Strikes through the text.</li>
        <li><code>[url]https://www.example.com[/url]</code> - Creates a link to the URL.</li>
        <li><code>[url=https://www.example.com]Link Text[/url]</code> - Creates a link with custom text.</li>
        <li><code>[code]Your code here[/code]</code> - Displays code in a monospaced font.</li>
        <li><code>[quote]Quoted text[/quote]</code> - Displays text as a quotation.</li>
        <li><code>[list]</code> and <code>[*]Item[/list]</code> - Creates an unordered list.
            <ul>
                <li><code>[*]First item</code></li>
                <li><code>[*]Second item</code></li>
            </ul>
        </li>
        <li><code>[list=1]</code> and <code>[*]Item[/list]</code> - Creates an ordered list (numbers).
            <ol>
                <li><code>[*]First item</code></li>
                <li><code>[*]Second item</code></li>
            </ol>
        </li>
    </ul>
    <p>Simply wrap the text you want to format with the corresponding BBCode tags.</p>
